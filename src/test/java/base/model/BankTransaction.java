package base.model;

/**
 * This class contains all details regarding the deserialization of Bank Transactions elements from API
 *
 */
public class BankTransaction {
    /**
     * Elements contained by User
     */
    private String createdAt;
    private String name;
    private String lastName;
    private int accountNumber;
    private float amount;
    private String type;
    private String email;
    private boolean active;
    private String country;
    private String telephone;
    private String id;

    public BankTransaction() {
    }

    //Setters and Getters generated


    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(int accountNumber) {
        this.accountNumber = accountNumber;
    }

    public float getAmount() {
        return amount;
    }

    public void setAmount(float amount) {
        this.amount = amount;
    }

    public String getType() {
        return type;
    }

    /**
     *
     * @author jesusDavid.Alvarez
     * @param type : String : withdrawal, deposit, invoice, payment
     */
    public void setType(String type) {
        this.type = type;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
